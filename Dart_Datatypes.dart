// | Data Type   | Description                                                                        |
// | ----------- | ---------------------------------------------------------------------------------- |
// | `num`       | Parent type of `int` and `double`                                                  |
// | `Object`    | The base class of all Dart classes                                                 |
// | `Function`  | Represents a function                                                              |
// | `Null`      | Represents the absence of a value                                                  |
// | `Future<T>` | Represents a value that will be available in the future (asynchronous programming) |
// | `Stream<T>` | Represents a sequence of asynchronous events                                       |
// | `Never`     | Indicates a value that never returns (e.g., throws an exception or infinite loop)  |
// | `Symbol`    | Used for reflective access to identifiers                                          |
// | `Runes`     | Represents Unicode code points                                                     |
// | `Uri`       | Used to represent and manipulate URIs                                              |
// | `Duration`  | Represents a span of time                                                          |

// | Data Type   | Description                                                              |
// | ----------- | ------------------------------------------------------------------------ |
// | `String`    | Sequence of characters (text)                                            |
// | `int`       | Whole numbers                                                            |
// | `double`    | Decimal numbers                                                          |
// | `bool`      | Boolean values: `true` or `false`                                        |
// | `List<T>`   | Ordered collection of items                                              |
// | `Map<K, V>` | Key-value pairs                                                          |
// | `Set<T>`    | Unordered collection of unique items                                     |
// | `dynamic`   | Can hold values of any type, type-checked at runtime                     |
// | `var`       | Automatically infers the type (not exactly a type, but worth mentioning) |
